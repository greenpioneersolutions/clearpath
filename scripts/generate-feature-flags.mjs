#!/usr/bin/env node
// Generates src/shared/featureFlags.generated.ts from features.json so the
// renderer, main process, and tests all read flag metadata from a single
// source of truth. Run on predev / prebuild / pretest hooks (see package.json).
//
// Why a script and not a Vite plugin alone: the generated TypeScript module
// gives us strong types (FeatureFlags union, BuildFlags constant, helper
// predicates) that the editor + tsc rely on. Vite's `define` substitution
// then layers a build-time literal on top for experimental flags so Rollup
// can dead-code-eliminate disabled experimental code paths.

import { readFileSync, writeFileSync, mkdirSync, existsSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const ROOT = resolve(__dirname, '..')
const FEATURES_PATH = resolve(ROOT, 'features.json')
const OUT_PATH = resolve(ROOT, 'src/shared/featureFlags.generated.ts')

function loadFeatures() {
  const raw = readFileSync(FEATURES_PATH, 'utf-8')
  const parsed = JSON.parse(raw)
  if (!parsed.flags || typeof parsed.flags !== 'object') {
    throw new Error('features.json: missing or invalid `flags` object')
  }
  for (const [key, def] of Object.entries(parsed.flags)) {
    if (typeof def.enabled !== 'boolean') {
      throw new Error(`features.json: flag "${key}" missing boolean "enabled"`)
    }
    if (typeof def.experimental !== 'boolean') {
      throw new Error(`features.json: flag "${key}" missing boolean "experimental"`)
    }
    if (typeof def.description !== 'string') {
      throw new Error(`features.json: flag "${key}" missing string "description"`)
    }
  }
  return parsed
}

function buildSource(features) {
  const keys = Object.keys(features.flags).sort()
  const expEnv =
    process.env.CLEARPATH_E2E_EXPERIMENTAL === '1' ||
    process.env.CLEARPATH_E2E_EXPERIMENTAL === 'true'

  const flagLines = keys.map((k) => {
    const def = features.flags[k]
    const effective = def.experimental && expEnv ? true : def.enabled
    return `  ${k}: ${effective},`
  })

  const meta = keys.map((k) => {
    const def = features.flags[k]
    return (
      `  ${k}: { experimental: ${def.experimental}, enabled: ${def.enabled}, ` +
      `addedIn: ${JSON.stringify(def.addedIn ?? null)} },`
    )
  })

  const experimentalKeys = keys.filter((k) => features.flags[k].experimental)
  const experimentalKeyLines = experimentalKeys.map((k) => `  '${k}',`)

  const banner = [
    '// AUTO-GENERATED by scripts/generate-feature-flags.mjs from features.json',
    '// Do not edit by hand. Re-run via `npm run generate:flags` or any build script.',
  ].join('\n')

  // Note: each `as const` assertion here is what gives consumers literal
  // boolean inference (so e.g. `if (BUILD_FLAGS.showPrScores) { … }` becomes a
  // statically known branch). Combined with the Vite `define` substitution
  // for `__FEATURES__`, Rollup eliminates disabled experimental branches.
  return `${banner}

export type FeatureFlagKey =
${keys.map((k) => `  | '${k}'`).join('\n')};

export type FeatureFlags = {
${keys.map((k) => `  ${k}: boolean;`).join('\n')}
};

export interface FeatureFlagMetadata {
  experimental: boolean;
  enabled: boolean;
  addedIn: string | null;
}

/**
 * Build-time effective values. Experimental flags reflect their
 * \`enabled\` from features.json unless CLEARPATH_E2E_EXPERIMENTAL=1 is set
 * at generation time, in which case every experimental flag is forced on.
 */
export const BUILD_FLAGS: Readonly<FeatureFlags> = Object.freeze({
${flagLines.join('\n')}
});

export const FEATURE_FLAG_META: Readonly<Record<FeatureFlagKey, FeatureFlagMetadata>> = Object.freeze({
${meta.join('\n')}
});

export const EXPERIMENTAL_FLAG_KEYS: readonly FeatureFlagKey[] = Object.freeze([
${experimentalKeyLines.join('\n')}
]);

export const FEATURE_FLAG_KEYS: readonly FeatureFlagKey[] = Object.freeze([
${keys.map((k) => `  '${k}',`).join('\n')}
]);

export function isExperimentalFlag(key: FeatureFlagKey): boolean {
  return FEATURE_FLAG_META[key].experimental;
}

/**
 * Build-time experimental gate. \`__FEATURES__\` is a Vite \`define\` constant
 * (see electron.vite.config.ts) — it is replaced with a literal boolean at
 * build time so Rollup can tree-shake disabled experimental branches.
 *
 * For non-experimental flags this returns the BUILD_FLAGS default; the
 * runtime FeatureFlagContext / featureFlagHandlers layer per-user
 * overrides on top of that.
 */
declare const __FEATURES__: Readonly<FeatureFlags>;
export function isExperimentalFlagEnabledAtBuild(key: FeatureFlagKey): boolean {
  // Reading through __FEATURES__ keeps the lookup statically replaceable.
  // eslint-disable-next-line no-restricted-globals
  return Boolean(__FEATURES__[key]);
}

export const FEATURE_SOURCE_VERSION = ${JSON.stringify(features.version ?? 1)} as const;
`
}

function main() {
  const features = loadFeatures()
  const out = buildSource(features)
  const outDir = dirname(OUT_PATH)
  if (!existsSync(outDir)) mkdirSync(outDir, { recursive: true })
  let prev = ''
  try {
    prev = readFileSync(OUT_PATH, 'utf-8')
  } catch {
    // First run — file does not exist yet
  }
  if (prev === out) return
  writeFileSync(OUT_PATH, out, 'utf-8')
  // Keep stdout terse so it doesn't clutter `npm run dev` logs
  // eslint-disable-next-line no-console
  console.log(`[feature-flags] wrote ${OUT_PATH}`)
}

main()
